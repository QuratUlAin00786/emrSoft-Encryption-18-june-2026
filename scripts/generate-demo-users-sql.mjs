const SALT_ROUNDS = 12;
const SCHEMA = process.env.DB_SCHEMA || "emrsoft_encrypted";
/** Replace with your tenant org id (emrSoft Healthcare / subdomain cura) */
const ORG_ID = Number(process.env.DEMO_ORG_ID || 1);

function sqlStr(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function buildInsert(user, organizationId, isSaaSOwner = false) {
  const workingDays = JSON.stringify(user.workingDays ?? []);
  const workingHours = JSON.stringify(user.workingHours ?? {});

  return `-- ${user.firstName} ${user.lastName} (${user.role}) — password: ${user.password}
INSERT INTO "${SCHEMA}".users (
  organization_id,
  email,
  username,
  password_hash,
  first_name,
  last_name,
  role,
  department,
  medical_specialty_category,
  sub_specialty,
  working_days,
  working_hours,
  permissions,
  is_active,
  is_saas_owner,
  created_at
)
SELECT
  ${organizationId},
  ${sqlStr(user.email)},
  ${sqlStr(user.username)},
  ${sqlStr(user.passwordHash)},
  ${sqlStr(user.firstName)},
  ${sqlStr(user.lastName)},
  ${sqlStr(user.role)},
  ${user.department ? sqlStr(user.department) : "NULL"},
  NULL,
  NULL,
  '${workingDays}'::jsonb,
  '${workingHours}'::jsonb,
  '{}'::jsonb,
  true,
  ${isSaaSOwner},
  NOW()
)
AND NOT EXISTS (
  SELECT 1 FROM "${SCHEMA}".users u
  WHERE u.organization_id = ${organizationId}
    AND (u.email = ${sqlStr(user.email)} OR u.username = ${sqlStr(user.username)})
);
`;
}

async function main() {
  const lines = [
    "-- =====================================================",
    "-- emrSoft demo users — generated by scripts/generate-demo-users-sql.mjs",
    `-- Schema: ${SCHEMA}`,
    `-- Tenant organization_id: ${ORG_ID} (subdomain: cura)`,
    "-- SaaS admin organization_id: 0",
    "-- bcrypt salt rounds: 12 (matches server authService)",
    "-- =====================================================",
    "",
    `SET search_path TO "${SCHEMA}";`,
    "",
  ];

  for (const user of DEMO_SEED_USERS) {
    const passwordHash = await bcrypt.hash(user.password, SALT_ROUNDS);
    lines.push(buildInsert({ ...user, passwordHash }, ORG_ID, false));
  }

  const saasHash = await bcrypt.hash(SAAS_SEED_USER.password, SALT_ROUNDS);
  lines.push(
    buildInsert(
      { ...SAAS_SEED_USER, passwordHash: saasHash },
      SAAS_SEED_USER.organizationId,
      true,
    ),
  );

  const outPath = path.join(root, "SQL Files", "emrsoft_demo_users_insert.sql");
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, lines.join("\n"), "utf8");
  console.log(`Wrote ${outPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
